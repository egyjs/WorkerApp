    /**
     * Complex: Should have 1 lowercase letter, 1 uppercase letter, 1 number, 1 special character and be at least 8 characters long
     */
    const PASSWORD_COMPLEX = '/(?=(.*[0-9]))(?=.*[\!@#$%^&*()\\\\[\]{}\-_+=~`|:;"\'<>,.\/?])(?=.*[a-z])(?=(.*[A-Z]))(?=(.*)).{8,}/';

